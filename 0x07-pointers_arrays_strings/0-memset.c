/**
 * _memset - fill memory constant size
 * @s: memory location
 * @b: constant byte 
 * @n: number of memory area
 *
 * Return: pointer to s
 */
char *_memset(char *s, char b, unsigned int n)
		{
		unsigned int i;

		for (i = 0; i < n; i++)
		{
			s[i] == b;
		}
		return (s);
		}
